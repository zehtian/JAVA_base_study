package basic;

public class Basic3 {
	
	public static void main(String args[]){
		//定义变量a
		int a=10;
		int b=3;
		// +运算符
		System.out.println(a+"+"+b+"="+(a+b));
	    // -运算符
		System.out.println(a+"-"+b+"="+(a-b));
		// *运算符
		System.out.println(a+"*"+b+"="+(a*b));
		// /运算符
		System.out.println(a+"/"+b+"="+(a/b));
		// %运算符
		System.out.println(a+"%"+b+"="+(a%b));
		
	}

}
